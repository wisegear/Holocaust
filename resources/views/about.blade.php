        <h2>About this site</h2>
        <p>I was never the most attentive in school, I left in 1988 with virtually no qualifications.  But whilst there it took 
            only a single lesson in history class to catch my attention.  It was on the holocaust.  "Seems unlikely" was my first 
            thought on contemplating that the Germans had killed a suggested 6m people.  Especially when they had done this pretty 
            much in plain sight whilst also fighting a world war and nobody knew or they would surely have done something about 
            it?</p>
        <p>And there it began.  A lifelong interest in learning more about the Holocaust.</p>
        <p>By the late 90's I built a website on the holocaust, it was a simple site using basic HTML pages and I just kept adding 
            more and more content to it.  Anyone who understands web sites will know that using only HTML and having hundreds of 
            pages was always going to end badly.  And it did!</p>
        <p>After many years I stopped adding content, I was no longer to control the content or change things in an easy way.  
            Adding a new menu item meant updating hundreds of pages.  So it eventually fell into a state disrepair and needed a 
            complete rebuild.</p>
        <p>Then life events happened, wife, children, mortgage payments and so on.  I never lost my interest in the subject though.  
            Whether sitting in the garden or lying on a beach on holiday there was always a book related to the Holocaust nearby.</p>   
        <p>More than 20 years after I created that first website I am back with this new one.  Approaching my 50th year and with 
            more time available I wanted to try again. I have had to learn new progamming languages, threatening to quit many times 
            in the past few months but ultimately got there.</p> 
        <p>This site is a personal one, transferring all of the information on that site from 20 years ago and adding more.  Very 
            much a non-academic style, I am not a well educated or confident person when it come to writing so you may or may not 
            appreciate what the site has to offer, but I tried!</p>
        <p class="font-bold">Lee Wisener</p>
